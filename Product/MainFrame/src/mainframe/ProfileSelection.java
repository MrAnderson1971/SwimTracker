    private void jButtonAddProfileActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonAddProfileActionPerformed
    {//GEN-HEADEREND:event_jButtonAddProfileActionPerformed
        if (Arrays.asList(Profile.getNames(profiles)).contains(jTextFieldName.getText()) && addProfileMode) // prevent multiple profiles using same name
        {
            // display error message if name already taken
            showWarningDialog("Name already taken.");
        } else if (jTextFieldName.getText().length() > 0 && jFormattedTextFieldAge.getText().length() > 0) // only proceed if all fields are filled
        {
            name = jTextFieldName.getText();
            age = Integer.parseInt(jFormattedTextFieldAge.getText().replaceAll(",", ""));
            gender = jComboBoxGender.getSelectedIndex() == 0 ? Gender.MALE : Gender.FEMALE;
            if (addProfileMode)
            {
                profiles.add(new Profile(name, age, gender));
            } else
            {
                String temp = activeProfile.getName();
                activeProfile.setName(name);
                if (Collections.frequency(new ArrayList<>(Arrays.asList(Profile.getNames(profiles))), name) > 1) // make sure that when editing profile, it doesn't take name of other profile apart from its own
                {
                    showWarningDialog("Name already taken.");
                    activeProfile.setName(temp); // set the profile back to its original name
                    return;
                }
                activeProfile.setAge(age);
                activeProfile.setGender(gender);
                jButtonAddProfile.setText("Add Profile");
                addProfileMode = true;
            }
            storeProfileData(profiles);
            jButtonCancel.setVisible(false);
            clearFields();
            jComboBoxProfiles.setModel(new javax.swing.DefaultComboBoxModel(Profile.getNames(profiles)));
        } else
        {
            showWarningDialog("Please complete the form.");
        }
    }//GEN-LAST:event_jButtonAddProfileActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonDeleteActionPerformed
    {//GEN-HEADEREND:event_jButtonDeleteActionPerformed

        if (showQuestionDialog("Delete Profile?"))
        {
            for (Profile p : profiles)
            {
                if (p.getName().equals(jComboBoxProfiles.getSelectedItem().toString()))
                {
                    profiles.remove(p);
                    jComboBoxProfiles.setModel(new javax.swing.DefaultComboBoxModel(Profile.getNames(profiles)));
                    break;
                }
            }
            addProfileMode = true;
            storeProfileData(profiles);
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonEditActionPerformed
    {//GEN-HEADEREND:event_jButtonEditActionPerformed
        if (jComboBoxProfiles.getSelectedItem() != null)
        {
            addProfileMode = false;
            activeProfile = Profile.getProfileWithName(profiles, jComboBoxProfiles.getSelectedItem().toString());
            jTextFieldName.setText(activeProfile.getName());
            jFormattedTextFieldAge.setText(Integer.toString(activeProfile.getAge()));
            jComboBoxGender.setSelectedIndex(activeProfile.getGender() == Gender.MALE ? 0 : 1);
            jButtonAddProfile.setText("Edit Profile");
            jButtonCancel.setVisible(true);
        } else
        {
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

