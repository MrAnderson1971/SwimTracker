    /**
     * Displays warning dialog.
     *
     * @param msg The text in the dialog.
     */
    public static void showWarningDialog(String msg)
    {
        final JOptionPane warning = new JOptionPane(msg, JOptionPane.ERROR_MESSAGE, JOptionPane.DEFAULT_OPTION);
        JOptionPane.showMessageDialog(warning, msg);
    }

    /**
     * Shows yes/no question dialog
     *
     * @param msg The text in the dialog.
     * @return true if the user clicked 'yes'.
     */
    public static boolean showQuestionDialog(String msg)
    {
        final JOptionPane optionPane = new JOptionPane(msg, JOptionPane.QUESTION_MESSAGE, JOptionPane.YES_NO_OPTION);
        int n = JOptionPane.showConfirmDialog(optionPane, msg, msg, JOptionPane.YES_NO_OPTION);
        return n == 0;
    }

